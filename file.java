public class HelloMath {

    public static void main(String[] args) {

        // Print Hello World
        System.out.println("Hello World");

        // Declare variables
        int a = 20;
        int b = 10;

        // Addition
        int sum = a + b;
        System.out.println("Addition: " + sum);

        // Subtraction
        int sub = a - b;
        System.out.println("Subtraction: " + sub);
    }
}
